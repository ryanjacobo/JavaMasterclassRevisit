package Enum;

// An enum, in its simplest form, is described like a class, but the keyword enum, replaces the keyword class. You can name the enum with any valid identifier, but like a class, Upper CamelCase is the preferred style.
// Examples are MonthOfTheYear, DirectionsInACompass, Sizes - EXTRA_SMALL, SMALL, MEDIUM, LARGE, EXTRA_LARGE
public enum DayOfTheWeek {
    SUN, MON, TUES, WED, THURS, FRI, SAT
}


